# 1、什么是函数：
# 一系列python语句的组合，可以在程序中运行一次或者多次，
# 一般是完成具体的独立的功能

# 2、为什么要使用函数：
# 代码的复用最大化、最小化冗余代码；整体代码结构清晰，问题局部化

# 函数定义规则：
# def 函数名(参数列表，可以是0-n个):
#     函数体【一系列的python语句，表示独立的功能】

# 函数调用：
# 本质上就是去执行函数定义里面的代码块，在调用之前，必须先定义
# 函数名加() 即可调用函数

# 函数的四种基本类型——分类：根据函数的返回值和函数的参数
# 有参数无返回值的
# 有参数有返回值的
# 无参数无返回值的
# 无参数有返回值的
