# 1、序列
# 在Python中，序列就是一组按照顺序排列的值【数据集合】
# 在Python中，存在三种内置的序列类型：
# 字符串、列表、元组
# 优点：可以支持索引和切片的操作
# 特征：第一个正索引为0，指向的是左端，第一个索引为负数的时候，指向的是右端（即从右边至左，-1开始）
# 切片：【高级特性】可以根据下标，来获取序列对象的任意【部分】数据
# 语法结构：[start：end：step]step步长，默认为1

# 2、list
# python当中非常重要的数据结构，是一种有序的数据集合
# 特点：
# (1)支持增删改查
# (2)列表中的数据是变化的【数据项可以变化，内存地址不会改变】
# (3)用[]中括号来表示列表类型，数据项之间用逗号来分割，注意：数据项可以是任意的数据类型
# (4)支持索引 和 切片来进行操作

