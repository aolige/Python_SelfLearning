# 是否存在：in
# # 字符存在in
strA = '刘浪1221312'
print("浪" in strA)
# # 列表存在in
listA = list(range(10))
print(9 in listA)
print(11 in listA)
# # 字典默认判断 键key 是否存在：in
dictA = {'name':'小牛','age':25,'sex':'男'}
print('age' in dictA)
