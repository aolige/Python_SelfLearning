#定义变量规则
#1变量名 = 数据
#2先定义变量，才能使用
#3变量是可以多次赋值的【在程序执行的过程中，值可以改变的量】
#4变量就是用来存储数据的
a = 10
print(type(a))

str = "liulang"
print(type(str))

#高级变量类型
b = ()#元组类型
print(type(b))

c = []#列表类型
print(type(c))

d = {}#字典类型
print(type(d))

